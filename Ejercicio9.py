#Escriba un programa que, dada una lista, devuelve una nueva lista cuyo contenido sea igual a la
#original pero invertida. Así, dada la lista ['Di', 'buen', 'día', 'a', 'papa'], deberá devolver ['papa', 'a',
#'día', 'buen', 'Di'].

lista1 = ["Hola", "a", "todos", "los", "presentes"]
print("Este es el orden normal de la lista:")
print(lista1)

lista1.reverse()
print("Este es el orden invertido de la lista usando reverse():")
print(lista1)

